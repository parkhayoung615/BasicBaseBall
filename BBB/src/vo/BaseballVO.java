package vo;

public class BaseballVO {
	private String bbId;
	private String bbPosition;
	private String bbTitle;
	private String bbContents;
	private String bbWrite;
	private String bbYn;
	
	public BaseballVO() {
		super();
	}

	public String getBbId() {
		return bbId;
	}

	public void setBbId(String bbId) {
		this.bbId = bbId;
	}

	public String getBbPosition() {
		return bbPosition;
	}

	public void setBbPosition(String strings) {
		this.bbPosition = strings;
	}

	public String getBbTitle() {
		return bbTitle;
	}

	public void setBbTitle(String bbTitle) {
		this.bbTitle = bbTitle;
	}

	public String getBbContents() {
		return bbContents;
	}

	public void setBbContents(String bbContents) {
		this.bbContents = bbContents;
	}

	public String getBbWrite() {
		return bbWrite;
	}

	public void setBbWrite(String bbWrite) {
		this.bbWrite = bbWrite;
	}

	public String getBbYn() {
		return bbYn;
	}

	public void setBbYn(String bbYn) {
		this.bbYn = bbYn;
	}
	
	
	

}
